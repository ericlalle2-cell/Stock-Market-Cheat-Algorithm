"""
Database Models for Stock Trading Advisor
Complete SQLAlchemy models with relationships and utilities
"""

from datetime import datetime
from sqlalchemy import (
    create_engine, Column, Integer, Float, String, 
    DateTime, Boolean, ForeignKey, Text, JSON, Index
)
from sqlalchemy.ext.declarative import declarative_base
from sqlalchemy.orm import relationship, sessionmaker, Session
from typing import Optional, List
import logging

# Set up logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

Base = declarative_base()


class Stock(Base):
    """Stock metadata and company information"""
    __tablename__ = 'stocks'
    
    id = Column(Integer, primary_key=True)
    symbol = Column(String(10), unique=True, nullable=False, index=True)
    name = Column(String(255))
    sector = Column(String(100))
    industry = Column(String(100))
    market_cap = Column(Float)
    description = Column(Text)
    is_active = Column(Boolean, default=True)
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    
    # Relationships
    prices = relationship('StockPrice', back_populates='stock', cascade='all, delete-orphan')
    indicators = relationship('TechnicalIndicator', back_populates='stock', cascade='all, delete-orphan')
    signals = relationship('Signal', back_populates='stock', cascade='all, delete-orphan')
    trades = relationship('Trade', back_populates='stock', cascade='all, delete-orphan')
    
    def __repr__(self):
        return f"<Stock(symbol='{self.symbol}', name='{self.name}')>"


class StockPrice(Base):
    """Historical and real-time price data"""
    __tablename__ = 'stock_prices'
    __table_args__ = (
        Index('idx_stock_timestamp', 'stock_id', 'timestamp'),
    )
    
    id = Column(Integer, primary_key=True)
    stock_id = Column(Integer, ForeignKey('stocks.id'), nullable=False, index=True)
    timestamp = Column(DateTime, nullable=False, index=True)
    
    # OHLCV data
    open = Column(Float, nullable=False)
    high = Column(Float, nullable=False)
    low = Column(Float, nullable=False)
    close = Column(Float, nullable=False)
    volume = Column(Integer, nullable=False)
    adjusted_close = Column(Float)
    
    created_at = Column(DateTime, default=datetime.utcnow)
    
    # Relationships
    stock = relationship('Stock', back_populates='prices')
    
    def __repr__(self):
        return f"<StockPrice(symbol='{self.stock.symbol}', date='{self.timestamp.date()}', close={self.close})>"


