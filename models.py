class DatabaseManager:
    """Manages database connections, sessions, and operations"""
    
    def __init__(self, connection_string: str):
        """
        Initialize database manager
        
        Args:
            connection_string: SQLAlchemy connection string
        """
        self.connection_string = connection_string
        self.engine = None
        self.SessionLocal = None
        self._initialize_engine()
    
    def _initialize_engine(self):
        """Initialize SQLAlchemy engine"""
        try:
            # Create engine with proper settings
            if 'sqlite' in self.connection_string:
                # SQLite-specific settings
                self.engine = create_engine(
                    self.connection_string,
                    echo=False,
                    connect_args={'check_same_thread': False}
                )
            else:
                # PostgreSQL/MySQL settings
                self.engine = create_engine(
                    self.connection_string,
                    echo=False,
                    pool_size=5,
                    max_overflow=10,
                    pool_pre_ping=True
                )
            
            self.SessionLocal = sessionmaker(bind=self.engine, autoflush=False, autocommit=False)
            logger.info("✓ Database engine initialized")
        
        except Exception as e:
            logger.error(f"Error initializing database engine: {e}")
            raise
    
    def create_tables(self):
        """Create all tables in the database"""
        try:
            Base.metadata.create_all(self.engine)
            logger.info("✓ Database tables created successfully")
        except Exception as e:
            logger.error(f"Error creating tables: {e}")
            raise
    
    def drop_tables(self):
        """Drop all tables (use with caution!)"""
        try:
            Base.metadata.drop_all(self.engine)
            logger.info("✓ Database tables dropped")
        except Exception as e:
            logger.error(f"Error dropping tables: {e}")
            raise
    
    def get_session(self) -> Session:
        """
        Get a new database session
        
        Returns:
            SQLAlchemy Session object
        """
        return self.SessionLocal()
    
    def close(self):
        """Close database connection"""
        if self.engine:
            self.engine.dispose()
            logger.info("✓ Database connection closed")
    
    # Helper methods for common operations
    
    def add_stock(self, session: Session, symbol: str, name: str = None, 
                  sector: str = None, industry: str = None) -> Stock:
        """
        Add or update a stock in the database
        
        Args:
            session: Database session
            symbol: Stock ticker symbol
            name: Company name
            sector: Industry sector
            industry: Specific industry
        
        Returns:
            Stock object
        """
        try:
            # Check if stock exists
            stock = session.query(Stock).filter_by(symbol=symbol).first()
            
            if stock:
                # Update existing stock
                if name:
                    stock.name = name
                if sector:
                    stock.sector = sector
                if industry:
                    stock.industry = industry
                stock.updated_at = datetime.utcnow()
            else:
                # Create new stock
                stock = Stock(
                    symbol=symbol,
                    name=name or symbol,
                    sector=sector,
                    industry=industry
                )
                session.add(stock)
            
            session.commit()
            logger.debug(f"Stock {symbol} added/updated")
            return stock
        
        except Exception as e:
            session.rollback()
            logger.error(f"Error adding stock {symbol}: {e}")
            raise
    
    def add_signal(self, session: Session, stock_id: int, signal_type: str,
                   confidence: float, current_price: float, **kwargs) -> Signal:
        """
        Add a trading signal to the database
        
        Args:
            session: Database session
            stock_id: Stock ID
            signal_type: 'BUY' or 'SELL'
            confidence: Signal confidence (0-1)
            current_price: Current stock price
            **kwargs: Additional signal parameters
        
        Returns:
            Signal object
        """
        try:
            signal = Signal(
                stock_id=stock_id,
                signal_date=datetime.utcnow(),
                signal_type=signal_type.upper(),
                confidence=confidence,
                current_price=current_price,
                **kwargs
            )
            
            session.add(signal)
            session.commit()
            logger.debug(f"Signal added: {signal_type} for stock_id {stock_id}")
            return signal
        
        except Exception as e:
            session.rollback()
            logger.error(f"Error adding signal: {e}")
            raise
    
    def get_active_signals(self, session: Session, stock_id: Optional[int] = None) -> List[Signal]:
        """
        Get active signals
        
        Args:
            session: Database session
            stock_id: Optional stock ID filter
        
        Returns:
            List of Signal objects
        """
        query = session.query(Signal).filter_by(is_active=True, is_executed=False)
        
        if stock_id:
            query = query.filter_by(stock_id=stock_id)
        
        return query.order_by(Signal.signal_date.desc()).all()
    
    def log_event(self, session: Session, level: str, module: str, 
                  message: str, details: dict = None):
        """
        Log system event to database
        
        Args:
            session: Database session
            level: Log level (DEBUG, INFO, WARNING, ERROR, CRITICAL)
            module: Module name
            message: Log message
            details: Additional details as dict
        """
        try:
            log = SystemLog(
                log_level=level.upper(),
                module=module,
                message=message,
                details=details
            )
            session.add(log)
            session.commit()
        except Exception as e:
            logger.error(f"Error logging to database: {e}")


