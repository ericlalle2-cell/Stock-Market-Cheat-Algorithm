class Signal(Base):
    """Buy/Sell signals generated by the system"""
    __tablename__ = 'signals'
    __table_args__ = (
        Index('idx_signal_stock_date', 'stock_id', 'signal_date'),
    )
    
    id = Column(Integer, primary_key=True)
    stock_id = Column(Integer, ForeignKey('stocks.id'), nullable=False, index=True)
    signal_date = Column(DateTime, nullable=False, index=True)
    signal_type = Column(String(10), nullable=False)  # 'BUY' or 'SELL'
    
    # Signal strength
    confidence = Column(Float, nullable=False)  # 0 to 1
    strength = Column(String(20))  # 'weak', 'moderate', 'strong'
    
    # Price information
    current_price = Column(Float, nullable=False)
    target_price = Column(Float)
    stop_loss_price = Column(Float)
    
    # Reasoning
    primary_reason = Column(String(255))
    technical_score = Column(Float)
    
    # Additional details (JSON)
    details = Column(JSON)
    
    # Status tracking
    is_active = Column(Boolean, default=True)
    is_executed = Column(Boolean, default=False)
    executed_at = Column(DateTime)
    
    created_at = Column(DateTime, default=datetime.utcnow)
    
    # Relationships
    stock = relationship('Stock', back_populates='signals')
    trades = relationship('Trade', back_populates='signal')
    
    def __repr__(self):
        return f"<Signal(symbol='{self.stock.symbol}', type='{self.signal_type}', confidence={self.confidence:.2f})>"


class Trade(Base):
    """Executed trades (paper or live)"""
    __tablename__ = 'trades'
    __table_args__ = (
        Index('idx_trade_stock_date', 'stock_id', 'trade_date'),
    )
    
    id = Column(Integer, primary_key=True)
    stock_id = Column(Integer, ForeignKey('stocks.id'), nullable=False, index=True)
    signal_id = Column(Integer, ForeignKey('signals.id'), index=True)
    
    # Trade details
    trade_type = Column(String(10), nullable=False)  # 'BUY' or 'SELL'
    trade_date = Column(DateTime, nullable=False, index=True)
    shares = Column(Integer, nullable=False)
    price = Column(Float, nullable=False)
    total_value = Column(Float, nullable=False)
    commission = Column(Float, default=0.0)
    
    # Trade mode
    is_paper_trade = Column(Boolean, default=True)
    
    # For SELL trades - track P&L
    entry_price = Column(Float)
    profit_loss = Column(Float)
    profit_loss_percentage = Column(Float)
    
    # Exit reason (for sells)
    exit_reason = Column(String(50))  # 'take_profit', 'stop_loss', 'signal', 'manual'
    
    # Additional details
    notes = Column(Text)
    
    created_at = Column(DateTime, default=datetime.utcnow)
    
    # Relationships
    stock = relationship('Stock', back_populates='trades')
    signal = relationship('Signal', back_populates='trades')
    
    def __repr__(self):
        return f"<Trade(symbol='{self.stock.symbol}', type='{self.trade_type}', shares={self.shares}, price={self.price})>"


